DO $$
DECLARE
    current_table_name text;
    identity_status text;
    column_default_value text;
    sequence_name text;
    next_id bigint;
BEGIN
    FOREACH current_table_name IN ARRAY ARRAY[
        'users',
        'products',
        'cart_items',
        'orders',
        'order_items',
        'custom_requests',
        'tickets',
        'conversations',
        'messages',
        'email_verification_codes',
        'payment_transactions',
        'mockups',
        'mockup_feedbacks',
        'reviews'
    ]
    LOOP
        IF to_regclass(format('public.%I', current_table_name)) IS NULL THEN
            CONTINUE;
        END IF;

        SELECT is_identity, column_default
        INTO identity_status, column_default_value
        FROM information_schema.columns
        WHERE table_schema = 'public'
          AND table_name = current_table_name
          AND column_name = 'id';

        IF NOT FOUND THEN
            CONTINUE;
        END IF;

        IF identity_status = 'NO' AND column_default_value IS NULL THEN
            EXECUTE format(
                'ALTER TABLE public.%I ALTER COLUMN id ADD GENERATED BY DEFAULT AS IDENTITY',
                current_table_name
            );
        END IF;

        sequence_name := pg_get_serial_sequence(format('public.%I', current_table_name), 'id');
        IF sequence_name IS NOT NULL THEN
            EXECUTE format(
                'SELECT COALESCE(MAX(id), 0) + 1 FROM public.%I',
                current_table_name
            ) INTO next_id;
            PERFORM setval(sequence_name::regclass, next_id, false);
        END IF;
    END LOOP;
END
$$;
